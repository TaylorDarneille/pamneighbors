const styles = theme => ({
  root: {
    flexGrow: 1,
    background: '#e0e0e0'
  },
  grouping: {
  	display: 'flex',
  	justifyContent: 'space-around',
    padding: '0 75px'
  },
  text: {
    paddingTop: theme.spacing.unit * 8,
    paddingBottom: theme.spacing.unit * 5
  },
  mobileStepper: {
    background: '#e0e0e0',
    padding: theme.spacing.unit * 4
  }
});

class ServicesStepper extends React.Component {
  state = {
    activeStep: 0,
  };

  handleNext = () => {
    this.setState(prevState => ({
      activeStep: prevState.activeStep + 1,
    }));
  };

  handleBack = () => {
    this.setState(prevState => ({
      activeStep: prevState.activeStep - 1,
    }));
  };

  handleStepChange = activeStep => {
    this.setState({ activeStep });
  };

  render() {
    const { classes, theme } = this.props;
    const { activeStep } = this.state;
    /*const maxSteps = tutorialSteps.length;*/
    const maxSteps = tutorialSteps.length-5;

    return (
      <div className={classes.root}>
      <Typography className={classes.text} component="h1" variant="headline" align="center" gutterBottom>
        Our Services
      </Typography>
        <SwipeableViews
          axis={theme.direction === 'rtl' ? 'x-reverse' : 'x'}
          index={activeStep}
          onChangeIndex={this.handleStepChange}
          enableMouseEvents
        >
          {tutorialSteps.map((step, index) => (
            <div className={classes.grouping}>
            	<ServiceCard service={tutorialSteps[activeStep]}/>
            	<ServiceCard service={tutorialSteps[activeStep+1]}/>
            	<ServiceCard service={tutorialSteps[activeStep+2]}/>
            	<ServiceCard service={tutorialSteps[activeStep+3]}/>
            	<ServiceCard service={tutorialSteps[activeStep+4]}/>
            </div>
          ))}
        </SwipeableViews>
        <MobileStepper
          steps={maxSteps}
          position="static"
          activeStep={activeStep}
          className={classes.mobileStepper}
          nextButton={
            <Button size="small" onClick={this.handleNext} disabled={activeStep === maxSteps - 1} className={classes.btn}>
              Next
              {theme.direction === 'rtl' ? <KeyboardArrowLeft /> : <KeyboardArrowRight />}
            </Button>
          }
          backButton={
            <Button size="small" onClick={this.handleBack} disabled={activeStep === 0}>
              {theme.direction === 'rtl' ? <KeyboardArrowRight /> : <KeyboardArrowLeft />}
              Back
            </Button>
          }
        />
      </div>
    );
  }
}

ServicesStepper.propTypes = {
  classes: PropTypes.object.isRequired,
  theme: PropTypes.object.isRequired,
};

export default withStyles(styles, { withTheme: true })(ServicesStepper);
